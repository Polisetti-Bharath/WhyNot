export interface PatternAnalysis {
  commonMissingSkills: Array<{ skill: string; frequency: number }>;
  cgpaIssues: boolean;
  improvementPriorities: string[];
  industryInsights: string;
  progressTracking?: {
    previousAnalysisDate?: string;
    improvementsSince?: string[];
  };
}

export interface RejectionAnalysis {
  coreMismatch: string;
  keyMissingSkills: string[];
  resumeFeedback: string[];
  actionPlan: string[];
  sentiment: string;
  type?: 'RULE_BASED' | 'NON_RULE_BASED';
  violations?: Array<{category: string, description: string, expected: string, actual: string}>;
  skillGaps?: Array<{skill: string, required: string, studentLevel?: string, suggestion: string}>;
  improvementSuggestions?: string[];
}

